# Numbers
#
#
# Numbers have four types in Python.
#   Int,
#   float,
#   complex,
#   long.
#
# Most common:
#
# Integers: they are basically whole numbers
#
integer_number = 1
#
# Floats: they are basically decimals
#
float_number = 1.0
#
#
# You can also convert between ints and floats and strings using
# 'casting':
#
assert float(integer_number) == 1.0
assert int(float_number) == 1
string_number = '100'
assert int(string_number) == 100
#
# Arithmetic operators:
#
# Addition:         3 + 2
# Subtraction:      3 - 2
# Multiplication:   3 * 2
# Division:         3 / 2
# Floor Division:   3 // 2
# Exponent:         3 ** 2
# Modulus:          3 % 2
#
# Order of operations:
#
# PEMDAS: (Parenthesis, Exponentiation, Multiplication, Division,
#           Addition, Subtraction)
#
# Augmented assignments: e.g. +=, -=, *=, /=, **=
#
# Builtin functions to deal with numbers e.g. 'abs', 'round', 'ceil'
#
# Comparison:
#
# Equal:            3 == 2
# Not Equal:        3 != 2
# Greater Than:     3 > 2
# Less Than:        3 < 2
# Greater or Equal: 3 >= 2
# Less or Equal:    3 <= 2

